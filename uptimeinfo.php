	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;

		// Write header
		$Doc->ExportTableHeader();
		if ($Doc->Horizontal) { // Horizontal format, write header
			$Doc->BeginExportRow();
			if ($ExportPageType == "view") {
				if ($this->realmid->Exportable) $Doc->ExportCaption($this->realmid);
				if ($this->starttime->Exportable) $Doc->ExportCaption($this->starttime);
				if ($this->uptime->Exportable) $Doc->ExportCaption($this->uptime);
				if ($this->maxplayers->Exportable) $Doc->ExportCaption($this->maxplayers);
				if ($this->revision->Exportable) $Doc->ExportCaption($this->revision);
			} else {
			}
			$Doc->EndExportRow();
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
				if ($ExportPageType == "view") {
					if ($this->realmid->Exportable) $Doc->ExportField($this->realmid);
					if ($this->starttime->Exportable) $Doc->ExportField($this->starttime);
					if ($this->uptime->Exportable) $Doc->ExportField($this->uptime);
					if ($this->maxplayers->Exportable) $Doc->ExportField($this->maxplayers);
					if ($this->revision->Exportable) $Doc->ExportField($this->revision);
				} else {
				}
				$Doc->EndExportRow();
			}
			$Recordset->MoveNext();
		}
		$Doc->ExportTableFooter();
	}

