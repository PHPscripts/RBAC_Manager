	// Key filter
	function KeyFilter() {
		$sKeyFilter = $this->SqlKeyFilter();
		if (!is_numeric($this->realmid->CurrentValue))
			$sKeyFilter = "0=1"; // Invalid key
		$sKeyFilter = str_replace("@realmid@", ew_AdjustSql($this->realmid->CurrentValue), $sKeyFilter); // Replace key value
		if (!is_numeric($this->starttime->CurrentValue))
			$sKeyFilter = "0=1"; // Invalid key
		$sKeyFilter = str_replace("@starttime@", ew_AdjustSql($this->starttime->CurrentValue), $sKeyFilter); // Replace key value
		return $sKeyFilter;
	}

