	// Load old record
	function LoadOldRecord() {

		// Load key values from Session
		$bValidKey = TRUE;
		if (strval($this->getKey("realmid")) <> "")
			$this->realmid->CurrentValue = $this->getKey("realmid"); // realmid
		else
