	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	function ExportDocument(&$Doc, &$Recordset, $StartRec, $StopRec, $ExportPageType = "") {
		if (!$Recordset || !$Doc)
			return;

		// Write header
		$Doc->ExportTableHeader();
		if ($Doc->Horizontal) { // Horizontal format, write header
			$Doc->BeginExportRow();
			if ($ExportPageType == "view") {
				if ($this->time->Exportable) $Doc->ExportCaption($this->time);
				if ($this->realm->Exportable) $Doc->ExportCaption($this->realm);
				if ($this->type->Exportable) $Doc->ExportCaption($this->type);
				if ($this->string->Exportable) $Doc->ExportCaption($this->string);
			} else {
				if ($this->time->Exportable) $Doc->ExportCaption($this->time);
				if ($this->realm->Exportable) $Doc->ExportCaption($this->realm);
				if ($this->type->Exportable) $Doc->ExportCaption($this->type);
				if ($this->string->Exportable) $Doc->ExportCaption($this->string);
			}
			$Doc->EndExportRow();
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
				if ($ExportPageType == "view") {
					if ($this->time->Exportable) $Doc->ExportField($this->time);
					if ($this->realm->Exportable) $Doc->ExportField($this->realm);
					if ($this->type->Exportable) $Doc->ExportField($this->type);
					if ($this->string->Exportable) $Doc->ExportField($this->string);
				} else {
					if ($this->time->Exportable) $Doc->ExportField($this->time);
					if ($this->realm->Exportable) $Doc->ExportField($this->realm);
					if ($this->type->Exportable) $Doc->ExportField($this->type);
					if ($this->string->Exportable) $Doc->ExportField($this->string);
				}
				$Doc->EndExportRow();
			}
			$Recordset->MoveNext();
		}
		$Doc->ExportTableFooter();
	}

